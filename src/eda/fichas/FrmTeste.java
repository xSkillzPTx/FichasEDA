     private void operacao(String op) {
        double a = Double.parseDouble(txtCR1.getText());
        double b = Double.parseDouble(txtCI1.getText());
        double c = Double.parseDouble(txtCR2.getText());
        double d = Double.parseDouble(txtCI2.getText());
        Complexo c1 = new Complexo(a,b);
        Complexo c2 = new Complexo(c,d);
        Complexo r;
        switch(op) {
            case "+"-> r = Complexo.somar(c1, c2);
            case "-"-> r = Complexo.subtrair(c1, c2);
            case "*"-> r = Complexo.multiplicar(c1, c2);
            case "/"-> r = Complexo.dividir(c1, c2);
            default->r = new Complexo();
        }
        
        
        txtCR3.setText(String.valueOf(r.getParteReal()));
        txtCI3.setText(String.valueOf(r.getParteImaginaria()));
    }
